package org.dvlyyon.nbi.model;

public class DObjectAttributeGrp {
	String mName = null;
	String mMap2 = null;
	
	public DObjectAttributeGrp(String mName, String mMap2) {
		super();
		this.mName = mName;
		this.mMap2 = mMap2;
	}

	public String getName() {
		return mName;
	}
	
	public String getMap2() {
		return mMap2;
	}

}
